    private void btnAmbilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAmbilActionPerformed
        //  untuk mengambil Saldo
        // Untuk mengambil saldo
    try {
        // Mengambil input dari pengguna
        double jumlah = Double.parseDouble(txtInput.getText());
        
        if (jumlah <= 0) {
            JOptionPane.showMessageDialog(this, "Masukkan jumlah yang valid!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Memeriksa apakah saldo cukup
        if (jumlah > saldo) {
            JOptionPane.showMessageDialog(this, "Saldo tidak mencukupi!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Mengurangi saldo
        saldo -= jumlah;
        totalSaldo.setText("Total Saldo: Rp " + saldo);

        // Menambahkan ke riwayat transaksi
        listModel.addElement("Kurang: Rp " + jumlah);

        // Mengosongkan input
        txtInput.setText("");
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Masukkan angka yang valid!", "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btnAmbilActionPerformed

