    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // Menambahkan saldo
        
    try {
        double jumlah = Double.parseDouble(txtInput.getText());
        if (jumlah <= 0) {
            JOptionPane.showMessageDialog(this, "Masukkan jumlah yang valid!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Tambahkan saldo
        saldo += jumlah;
        totalSaldo.setText("Total Saldo: Rp " + saldo);

        // Tambahkan ke riwayat transaksi
        listModel.addElement("Tambah: Rp " + jumlah);
        txtInput.setText("");
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Masukkan angka yang valid!", "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btnTambahActionPerformed

